#pega todos os titulos antes de 2000
def antes_2000(loja_discos, ano):
    lista = []
    for disco in loja_discos.values():
        if disco["ano"] < ano:
            lista.append(disco) #se eu guardar o disco inteiro
    return lista

#imprimir todos os discos de um genero
def discos_genero(loja_discos, genero):
    lista_genero = []
    for disco in loja_discos.values():
        if disco["genero"].lower() == genero.lower():
            lista_genero.append(disco)
    return lista_genero

#imprimir tudo de qualquer banda
def imprime_banda(loja_discos, banda):
    lista_banda = []
    for disco in loja_discos.values():
        if disco["banda"].lower() == banda.lower():
            lista_banda.append(disco)
    return lista_banda

#imprime tudo de qualquer gravadora
def imprime_gravadora(loja_discos, gravadora):
    lista_gravadora = []
    for disco in loja_discos.values():
        if disco["gravadora"].lower() == gravadora.lower():
            lista_gravadora.append(disco)
    return lista_gravadora

